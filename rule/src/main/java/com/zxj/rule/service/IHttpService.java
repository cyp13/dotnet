package com.zxj.rule.service;

import java.util.Map;

public interface IHttpService {

	/**
	 * 发送http请求
	 * @param url 请求地址
	 * @param parameter 请求参数
	 * @return
	 */
	Map<String, Object> post(String url, Map<String, Object> parameter);
	Map<String, Object> get(String url, Map<String, Object> parameter);
	
	/**
	 * 发送json
	 * @param url
	 * @param data
	 * @return
	 */
	Map<String, Object> postJSON(String url, Object data);
}
