package com.zxj.rule.model;

import java.util.List;

/**
 * 规则标题
 * @author zmk
 *
 */
public class RuleTitle {

	/**
	 * 使用包装类可以区分空和非空的情况
	 */
	private Long id;
    /**
     * 稽核标志，1表示稽核，0表示非稽核
     */
	private Integer auditFlag;
	/**
     * 是否抽样，1表示抽样，0表示不抽样
     */
	private Integer sample;
	/**
     * 考核范围，1渠道/组，0个人
     */
	private Integer useRange;
	/**
     * 规则状态，0启用，1停用
     */
	private Integer ruleStatus;
	/**
	 * 数据来源，0基于指标数量，1基于三方数据，2基于指标明细
	 */
	private Integer dataSource;
	/**
     * 考核单位，0绝对值，1百分率，2百分制
     */
	private Integer auditUnit;
	/**
     * 数据来源同步接口
     */
	private String dataLink;
	/**
     * 考核指标
     */
	private Integer auditTarget;
	/**
     * 所属机构id
     */
	private String orgId;
	/**
     * 所属机构编码
     */
	private String orgCode;
	/**
     * 业务类型，1渠道支撑平台，以后有其他系统的规则，往后加
     */
	private Integer serviceType;
	/**
     * 外部系统标志，用于绑定外部系统，例如存项目标识什么的
     */
	private String forignKey;
	/**
     * 版本号
     */
	private Integer ruleVersion;
	/**
	 * 规则明细列表
	 */
	private List<RuleDetail> ruleList;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Integer getAuditFlag() {
		return auditFlag;
	}
	public void setAuditFlag(Integer auditFlag) {
		this.auditFlag = auditFlag;
	}
	public Integer getSample() {
		return sample;
	}
	public void setSample(Integer sample) {
		this.sample = sample;
	}
	public Integer getUseRange() {
		return useRange;
	}
	public void setUseRange(Integer useRange) {
		this.useRange = useRange;
	}
	public Integer getRuleStatus() {
		return ruleStatus;
	}
	public void setRuleStatus(Integer ruleStatus) {
		this.ruleStatus = ruleStatus;
	}
	public Integer getAuditUnit() {
		return auditUnit;
	}
	public void setAuditUnit(Integer auditUnit) {
		this.auditUnit = auditUnit;
	}
	public String getDataLink() {
		return dataLink;
	}
	public void setDataLink(String dataLink) {
		this.dataLink = dataLink;
	}
	public Integer getAuditTarget() {
		return auditTarget;
	}
	public void setAuditTarget(Integer auditTarget) {
		this.auditTarget = auditTarget;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
	public String getOrgCode() {
		return orgCode;
	}
	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}
	public Integer getServiceType() {
		return serviceType;
	}
	public void setServiceType(Integer serviceType) {
		this.serviceType = serviceType;
	}
	public String getForignKey() {
		return forignKey;
	}
	public void setForignKey(String forignKey) {
		this.forignKey = forignKey;
	}
	public Integer getRuleVersion() {
		return ruleVersion;
	}
	public void setRuleVersion(Integer ruleVersion) {
		this.ruleVersion = ruleVersion;
	}
	public List<RuleDetail> getRuleList() {
		return ruleList;
	}
	public void setRuleList(List<RuleDetail> ruleList) {
		this.ruleList = ruleList;
	}
	public Integer getDataSource() {
		return dataSource;
	}
	public void setDataSource(Integer dataSource) {
		this.dataSource = dataSource;
	}
	
}
