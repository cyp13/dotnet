package com.zxj.rule.model;

/**
 * 基础规则
 * @author PC
 *
 */
public class RuleBase {

	private Long id;
	/**
	 * 1积分换算成多少元
	 */
	private Integer scoreValue;
	/**
	 * 周期类型，0按年度，1按半年，2按季度，3按月度，4自定义
	 */
	private Integer circleType;
	/**
	 * 考核类型：0组成员只满足一个，1组成员全部满足，2组成员平均值满足，3组成员按配置比例满足
	 */
	private Integer auditType;
	/**
	 * 考核标准
	 */
	private Integer auditValue;
	/**
	 * 0全国统一标准，1非全国统一标准
	 */
	private Integer useForAll;
	/**
	 * 所属机构id
	 */
	private String orgId;
	/**
	 * 所属机构编码
	 */
	private String orgCode;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public Integer getScoreValue() {
		return scoreValue;
	}
	public void setScoreValue(Integer scoreValue) {
		this.scoreValue = scoreValue;
	}
	public Integer getCircleType() {
		return circleType;
	}
	public void setCircleType(Integer circleType) {
		this.circleType = circleType;
	}
	public Integer getAuditType() {
		return auditType;
	}
	public void setAuditType(Integer auditType) {
		this.auditType = auditType;
	}
	public Integer getAuditValue() {
		return auditValue;
	}
	public void setAuditValue(Integer auditValue) {
		this.auditValue = auditValue;
	}
	public Integer getUseForAll() {
		return useForAll;
	}
	public void setUseForAll(Integer useForAll) {
		this.useForAll = useForAll;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
	public String getOrgCode() {
		return orgCode;
	}
	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}
	
}
